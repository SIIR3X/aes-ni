#include "unity/unity.h"
#include "aes/modes/aes_ctr.h"
#include "aes/core/aes_key_expansion.h"

void test_ctr_crypt_128(void)
{
	const uint8_t key[16] = {
		0x2b, 0x7e, 0x15, 0x16,
		0x28, 0xae, 0xd2, 0xa6,
		0xab, 0xf7, 0x15, 0x88,
		0x09, 0xcf, 0x4f, 0x3c
	};

	const uint8_t iv[16] = {
		0xf0, 0xf1, 0xf2, 0xf3,
		0xf4, 0xf5, 0xf6, 0xf7,
		0xf8, 0xf9, 0xfa, 0xfb,
		0xfc, 0xfd, 0xfe, 0xff
	};

	const uint8_t plaintext[16] = {
		0x6b, 0xc1, 0xbe, 0xe2,
		0x2e, 0x40, 0x9f, 0x96,
		0xe9, 0x3d, 0x7e, 0x11,
		0x73, 0x93, 0x17, 0x2a
	};

	const uint8_t expected[16] = {
		0x87, 0x4d, 0x61, 0x91,
		0xb6, 0x20, 0xe3, 0x26,
		0x1b, 0xef, 0x68, 0x64,
		0x99, 0x0d, 0xb6, 0xce
	};

	uint8_t output[16] = {0};
	aes_context_t ctx;

	TEST_ASSERT_EQUAL_INT(0, aes_context_init(&ctx, key, AES_128));
	aes_ctr_crypt(&ctx, iv, plaintext, 16, output);
	TEST_ASSERT_EQUAL_UINT8_ARRAY(expected, output, 16);
}


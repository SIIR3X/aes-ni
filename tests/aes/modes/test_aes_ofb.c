#include "unity/unity.h"
#include "aes/modes/aes_ofb.h"
#include "aes/core/aes_key_expansion.h"

void test_ofb_crypt_128(void)
{
	const uint8_t key[16] = {
		0x2b, 0x7e, 0x15, 0x16, 
		0x28, 0xae, 0xd2, 0xa6,
		0xab, 0xf7, 0x15, 0x88, 
		0x09, 0xcf, 0x4f, 0x3c
	};

	const uint8_t iv[16] = {
		0x00, 0x01, 0x02, 0x03, 
		0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0a, 0x0b, 
		0x0c, 0x0d, 0x0e, 0x0f
	};

	const uint8_t plaintext[16] = {
		0x6b, 0xc1, 0xbe, 0xe2, 
		0x2e, 0x40, 0x9f, 0x96,
		0xe9, 0x3d, 0x7e, 0x11, 
		0x73, 0x93, 0x17, 0x2a
	};

	const uint8_t expected[16] = {
		0x3b, 0x3f, 0xd9, 0x2e, 
		0xb7, 0x2d, 0xad, 0x20,
		0x33, 0x34, 0x49, 0xf8, 
		0xe8, 0x3c, 0xfb, 0x4a
	};

	uint8_t output[16] = {0};
	aes_context_t ctx;

	TEST_ASSERT_EQUAL_INT(0, aes_context_init(&ctx, key, AES_128));
	aes_ofb_crypt(&ctx, iv, plaintext, 16, output);
	TEST_ASSERT_EQUAL_UINT8_ARRAY(expected, output, 16);
}

